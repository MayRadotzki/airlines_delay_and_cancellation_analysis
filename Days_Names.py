def day_name(weekday):
    if weekday == 1:
        return "Sunday"
    if weekday == 2:
        return "Monday"
    if weekday == 3:
        return "Tuesday"
    if weekday == 4:
        return "Wednesday"
    if weekday == 5:
        return "Thursday"
    if weekday == 6:
        return "Friday"
    if weekday == 7:
        return "Saturday"